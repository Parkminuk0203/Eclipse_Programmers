package Programmers;

public class 두_수의_차 {

	public static void main(String[] args) {
		System.out.println(solution(2,3));
	} // end of main
	public static int solution(int num1, int num2) {
		int answer = 0;
		int difference = 0;
		
		difference = num1 - num2;
		answer = difference;
		
		return answer;
	}
} // end of class
